package com.loiane.cursojava.aula11;

public class ValoresDecHexOctBin {

	public static void main(String[] args) {

		int decVal = 26;
		
		int hexVal = 0x1a;
		
		int octVal = 032;
		
		int binVal = 0b11010;
		
		System.out.println("Decimal.... = " + decVal);
		System.out.println("Hexadecimal = " + hexVal);
		System.out.println("Octadecimal = " + octVal);
		System.out.println("Binário.... = " + binVal);
		

	}

}
